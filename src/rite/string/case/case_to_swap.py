def to_swap_case(text: str) -> str:
    """
    Swap the case of each character in the text.
    Example: 'Hello World' -> 'hELLO wORLD'

    Parameters:
    text (str): The text whose case is to be swapped.

    Returns
    -------
    str: The text with swapped case.
    """
    return text.swapcase()
